export const createProductController = async (req, res) => {
  try {
    const { name, description, price, category, quantity, shipping } =
      req.fields;
    const { photo } = req.files;
    //validacion 
    switch (true) {
      case !name:
        return res.status(500).send({ error: "Nombre es requerido" });
      case !description:
        return res.status(500).send({ error: "Descripcion es requerida" });
      case !price:
        return res.status(500).send({ error: "Precio es requerido" });
      case !category:
        return res.status(500).send({ error: "Categoria es requerida" });
      case !quantity:
        return res.status(500).send({ error: "Cantidad es requerida" });
      case photo && photo.size > 1000000:
        return res
          .status(500)
          .send({ error: "La imagen es requerida y debe ser de menos de 1mb" });
    }

    const products = new productModel({ ...req.fields, slug: slugify(name) });
    if (photo) {
      products.photo.data = fs.readFileSync(photo.path);
      products.photo.contentType = photo.type;
    }
    await products.save();
    res.status(201).send({
      success: true,
      message: "Producto creado exitosamente!",
      products,
    });
  } catch (error) {
    console.log(error);
    res.status(500).send({
      success: false,
      error,
      message: "Error al crear producto",
    });
  }
};

